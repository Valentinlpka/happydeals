class Event {
  String id;
  String title;
  String categories;
  String date;
  int like;
  String idCompany;
  String ville;
  String description;
  String localisation;

  Event({
    required this.id,
    required this.title,
    required this.categories,
    required this.date,
    required this.like,
    required this.idCompany,
    required this.ville,
    required this.description,
    required this.localisation,
  });
}
